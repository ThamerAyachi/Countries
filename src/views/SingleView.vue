<script>
import { numberFormat } from "../assets/methods.js";

export default {
	data() {
		return {
			countryName: this.$route.params.name.replaceAll("_", " "),
			country: {
				name: { common: "" },
				flags: { png: "https://flagcdn.com/w320/bg.png" },
				population: 0,
				region: "",
				subregion: "",
				capital: "",
				tld: [""],
			},
			currencies: {},
			currenciesName: "",
			languages: [],
			nativeName: "",
			borders: [],
			done: false,
		};
	},
	methods: {
		async getData() {
			try {
				const rest = await this.$store.dispatch(
					"getCountryByName",
					this.countryName
				);
				if (rest.status == 200) this.country = rest.data[0];

				this.currencies = rest.data[0].currencies;
				Object.keys(rest.data[0].languages).forEach((l) => {
					this.languages.push(rest.data[0].languages[l]);
				});
				this.nativeName =
					rest.data[0].name.nativeName[
						Object.keys(rest.data[0].name.nativeName)[0]
					].common;
				if (rest.data[0].borders) this.borders = rest.data[0].borders;
				this.done = true;
			} catch (e) {
				console.log(e);
			}

			this.country.population = numberFormat(this.country.population);
			this.currenciesName =
				this.currencies[Object.keys(this.currencies)[0]].name;
		},
		async getDataByParam(name) {
			this.done = false;
			try {
				const rest = await this.$store.dispatch("getCountryByName", name);
				if (rest.status == 200) this.country = rest.data[0];

				this.currencies = rest.data[0].currencies;
				Object.keys(rest.data[0].languages).forEach((l) => {
					this.languages.push(rest.data[0].languages[l]);
				});
				this.nativeName =
					rest.data[0].name.nativeName[
						Object.keys(rest.data[0].name.nativeName)[0]
					].common;
				if (rest.data[0].borders) this.borders = rest.data[0].borders;
				else this.borders = [];

				this.done = true;
			} catch (e) {
				console.log(e);
			}

			this.country.population = numberFormat(this.country.population);
			this.currenciesName =
				this.currencies[Object.keys(this.currencies)[0]].name;
		},
	},
	async mounted() {
		await this.getData();
		console.log(this.borders);
	},
};
</script>

