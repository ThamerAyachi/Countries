<template>
	<div class="2xl:mx-52 xl:mx-32 mx-5 my-10">
		<!-- button to go back -->
		<div class="flex text-sm">
			<router-link
				to="/"
				class="flex items-center space-x-3 shadow-md rounded bg-white py-1 px-5"
			>
				<svg
					xmlns="http://www.w3.org/2000/svg"
					fill="none"
					viewBox="0 0 24 24"
					stroke-width="1.5"
					stroke="currentColor"
					class="w-4 h-4"
				>
					<path
						stroke-linecap="round"
						stroke-linejoin="round"
						d="M10.5 19.5L3 12m0 0l7.5-7.5M3 12h18"
					/>
				</svg>

				<span>Back</span></router-link
			>
		</div>

		<!-- element -->
		<div class="grid lg:grid-cols-2 grid-cols-1 gap-4 my-16" v-if="done">
			<div class="w-full flex justify-center">
				<img :src="country.flags.svg" alt="" class="max-h-80" />
			</div>
			<div class="lg:p-5">
				<p class="text-2xl font-bold my-5 lg:mb-5 lg:my-0">
					{{ country.name.common }}
				</p>

				<!-- content grid -->
				<div class="grid md:grid-cols-2 grid-cols-1 space-y-5 md:space-y-0">
					<!-- part one -->
					<div class="space-y-2">
						<p>
							<span class="font-semibold">Native Name: </span>
							<span>
								{{ nativeName }}
							</span>
						</p>
						<p>
							<span class="font-semibold">Population: </span>
							<span>
								{{ country.population }}
							</span>
						</p>
						<p>
							<span class="font-semibold">Region: </span>
							<span>
								{{ country.region }}
							</span>
						</p>
						<p>
							<span class="font-semibold">Sup Region: </span>
							<span>
								{{ country.subregion }}
							</span>
						</p>
						<p>
							<span class="font-semibold">Capital: </span>
							<span>
								{{ country.capital[0] }}
							</span>
						</p>
					</div>
					<!-- part two -->
					<div class="space-y-2">
						<p>
							<span class="font-semibold">Top Level Domain: </span>
							<span>
								{{ country.tld[0] }}
							</span>
						</p>
						<p>
							<span class="font-semibold">Currencies: </span>
							<span>
								{{ currenciesName }}
							</span>
						</p>
						<p>
							<span class="font-semibold">Languages: </span>
							<span v-for="(l, i) in languages" :key="i">
								{{ l + (languages[i + 1] ? ", " : "") }}
							</span>
						</p>
					</div>
				</div>
				<div
					class="flex my-10 space-x-2 items-center"
					v-if="borders.length != 0"
				>
					<p class="font-semibold">Border Countries:</p>
					<router-link
						:to="b"
						@click="getDataByParam(b)"
						v-for="(b, j) in borders"
						:key="j"
						class="bg-white rounded shadow px-2 py-1"
					>
						{{ b }}
					</router-link>
				</div>
			</div>
		</div>
		<div v-else class="w-full flex justify-center my-32">
			<svg
				aria-hidden="true"
				class="mr-2 w-8 h-8 text-gray-200 animate-spin dark:text-gray-600 fill-blue-600"
